// Generates the USDA whole-foods seed SQL from the curated CSV (#72).
//
// Run with Node's type-stripping so it can import the pure transform straight
// from @leanlog/data-access source without a build step:
//
//   node --experimental-strip-types scripts/build-usda-seed.mjs
//
// The CSV is the checked-in source of truth (R9); the emitted .sql is a generated
// artifact (gitignored) that the seed pipeline executes with INSERT OR REPLACE.

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
// Direct .ts import: data-access/src/seed.ts has only type-only relative imports,
// so Node's --experimental-strip-types loads it without extension resolution.
import { buildUsdaSeedRow, usdaSeedInsertStatement } from '../../data-access/src/seed.ts';

const here = dirname(fileURLToPath(import.meta.url));
const csvPath = resolve(here, '../seed/usda-whole-foods.csv');
const sqlPath = resolve(here, '../seed/usda-whole-foods.sql');

// Minimal RFC-4180 CSV parser: handles quoted fields, escaped "" quotes, commas
// and newlines inside quotes. Sufficient for the curated USDA export.
function parseCsv(text) {
  const rows = [];
  let row = [];
  let field = '';
  let inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') {
          field += '"';
          i++;
        } else {
          inQuotes = false;
        }
      } else {
        field += c;
      }
    } else if (c === '"') {
      inQuotes = true;
    } else if (c === ',') {
      row.push(field);
      field = '';
    } else if (c === '\n') {
      row.push(field);
      rows.push(row);
      row = [];
      field = '';
    } else if (c !== '\r') {
      field += c;
    }
  }
  // Flush the trailing field/row when the file has no final newline.
  if (field.length > 0 || row.length > 0) {
    row.push(field);
    rows.push(row);
  }
  return rows;
}

const text = readFileSync(csvPath, 'utf8');
const records = parseCsv(text).filter((r) => r.some((cell) => cell.trim() !== ''));
const [header, ...dataRows] = records;

const seen = new Set();
const statements = [];
for (const cells of dataRows) {
  const obj = {};
  header.forEach((col, idx) => {
    obj[col] = cells[idx] ?? '';
  });
  const seed = buildUsdaSeedRow(obj);
  if (seen.has(seed.id)) {
    throw new Error(`Duplicate seed id for FDC_ID ${obj.FDC_ID} — FDC_IDs must be unique`);
  }
  seen.add(seed.id);
  statements.push(usdaSeedInsertStatement(seed));
}

const banner = `-- GENERATED by scripts/build-usda-seed.mjs from seed/usda-whole-foods.csv — do not edit.\n-- ${statements.length} USDA whole foods (#72).\n`;
writeFileSync(sqlPath, `${banner}${statements.join('\n')}\n`);
console.log(`Wrote ${statements.length} USDA seed statements to ${sqlPath}`);
